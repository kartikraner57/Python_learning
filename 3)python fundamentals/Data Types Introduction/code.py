# Integer
x = 5
print(x)  # Output: 5
print(type(x))  # Output: <class 'int'>

# Float
y = 3.14
print(y)  # Output: 3.14
print(type(y))  # Output: <class 'float'>

# String
name = "Alice"
print(name)  # Output: Alice
print(type(name))  # Output: <class 'str'>

# Boolean
is_active = True
print(is_active)  # Output: True
print(type(is_active))  # Output: <class 'bool'>

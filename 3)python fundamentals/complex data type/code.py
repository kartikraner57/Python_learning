# Defining a complex number
z = 3 + 4j  # 3 is the real part, 4 is the imaginary part
print(z)  # Output: (3+4j)

z1 = 3 + 4j
z2 = 1 + 2j
result = z1 + z2
print(result)  # Output: (4+6j)

result = z1 - z2
print(result)  # Output: (2+2j)

result = z1 * z2
print(result)  # Output: (-5+10j)

result = z1 / z2
print(result)  # Output: (2.2+0.4j)
  